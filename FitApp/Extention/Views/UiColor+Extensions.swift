import UIKit

extension UIColor {
	
	@nonobjc class var projectTail: UIColor {
		return UIColor(red: 17.0 / 255.0, green: 121.0 / 255.0, blue: 115.0 / 255.0, alpha: 1.0)
	}
	
	@nonobjc class var projectGreen: UIColor {
		return UIColor(red: 72.0 / 255.0, green: 149.0 / 255.0, blue: 43.0 / 255.0, alpha: 1.0)
	}
	
	@nonobjc class var projectTurquoise: UIColor {
		return UIColor(red: 153.0 / 255.0, green: 207.0 / 255.0, blue: 204.0 / 255.0, alpha: 1.0)
	}
	@nonobjc class var blackShadow: UIColor {
	  return UIColor(white: 0.0, alpha: 0.16)
	}
	@nonobjc class var projectBackgroundColor: UIColor {
		return #colorLiteral(red: 0.9568627477, green: 0.9568628669, blue: 0.9568627477, alpha: 1)
	}
}
